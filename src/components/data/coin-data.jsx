// Utility functions
export const getCoinItems = (count, filterFn = null) => {
  let filteredCoins = filterFn ? coinData.filter(filterFn) : coinData;
  return filteredCoins.slice(0, count);
};

export const getTopMovers = (count = 4) => {
  return getCoinItems(count, (coin) => Math.abs(coin.priceChange) > 1);
};

export const getMarketUpdates = (count = 8) => {
  return getCoinItems(count);
};

export const getCoinById = (id) => {
  return coinData.find((coin) => coin.id === Number(id));
};

export const getCoinsByCategory = (category) => {
  if (category === "All") return coinData;
  return coinData.filter((coin) => coin.categories.includes(category));
};

export const getCategories = () => {
  const categories = new Set(["All"]);
  coinData.forEach((coin) => {
    coin.categories.forEach((category) => categories.add(category));
  });
  return Array.from(categories);
};
