export const getNewsItems = (count, filterFn = null) => {
  let filteredNews = filterFn ? newsData.filter(filterFn) : newsData;
  return filteredNews.slice(0, count);
};

export const getTrendingNews = (count = 7) => {
  return getNewsItems(count, (item) => item.isTrending);
};

export const getLatestNews = (count = 7) => {
  return getNewsItems(count);
};

export const getCategories = () => {
  const allCategories = newsData.reduce((acc, news) => {
    news.categories.forEach((category) => acc.add(category));
    return acc;
  }, new Set());

  return [
    {
      id: "all",
      label: "All News",
      color: "from-gray-400/10 to-gray-600/10",
    },
    ...Array.from(allCategories).map((category) => ({
      id: category.toLowerCase().replace(/\s+/g, "-"),
      label: category,
      color: getCategoryColor(category),
    })),
  ];
};

function getCategoryColor(category) {
  const colors = {
    Bitcoin: "from-orange-400/10 to-orange-600/10",
    Ethereum: "from-blue-400/10 to-blue-600/10",
    Altcoin: "from-green-400/10 to-green-600/10",
    DeFi: "from-purple-400/10 to-purple-600/10",
    NFT: "from-pink-400/10 to-pink-600/10",
    Exchange: "from-yellow-400/10 to-yellow-600/10",
    Regulations: "from-red-400/10 to-red-600/10",
    "Market Analysis": "from-cyan-400/10 to-cyan-600/10",
  };
  return colors[category] || "from-violet-400/10 to-violet-600/10";
}
